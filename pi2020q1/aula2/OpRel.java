public class OpRel
{
    public static void main(String[] args)
    {
        int a = 5;
        int b = 7;
        boolean x;

        x = a == b;
        System.out.println(x);
        x = a != b;
        System.out.println(x);
        x = a < b;
        System.out.println(x);
        x = a > b;
        System.out.println(x);
        x = a <= b;
        System.out.println(x);
        x = a >= b;
        System.out.println(x);
    }
}